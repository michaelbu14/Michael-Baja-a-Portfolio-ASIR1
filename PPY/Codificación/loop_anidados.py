'''Loop anidado'''


for n in range(10):  # Del 0 al 9 para hacer su tabla
    for m in range(11):
        print(n, 'x', m, '=', ' ', n*m)
    print("**********************")


for n in range(3):
    for m in range(2):
        print(f'{n},{m}')
    print("**********************")


for i in range(3):
    for j in range(2):
        print(f'{i},{j}')
    print("**********************")
